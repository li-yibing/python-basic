"""至少两种方式输出字符串"""

# 直接打印
print("Nice to meet you Python")

# 赋值并打印
character = "Nice to meet you Python"
print(character)

"""输入字符串，并输出字符串"""

# 使用input函数
character = input("请输入：Nice to meet you Python\n")
print(character)

"""从键盘输入一个整数和一个字符"""
int_character = input("请输入一个整数：")
char_character = input("请输入一个字符：")
print("你输入的整数是：", int_character)
print("你输入的字符是：", char_character)